package com.gmail.radzkovevgeni.model;

public class Train {
    private String edit1;
    private String edit2;
    private String edit3;

    public Train(String edit1, String edit2, String edit3) {
        this.edit1 = edit1;
        this.edit2 = edit2;
        this.edit3 = edit3;
    }

    public String getEdit1() {
        return edit1;
    }

    public String getEdit2() {
        return edit2;
    }

    public String getEdit3() {
        return edit3;
    }
}
